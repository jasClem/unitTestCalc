class Calculator():


    def add(self, firstNumber, secondNumber):
        # Add numbers
        return firstNumber + secondNumber

    def subtract(self, firstNumber, secondNumber):
        # Subtract numbers
        return firstNumber - secondNumber

    def multiply(self, firstNumber, secondNumber):
        # Multiply numbers
        return firstNumber * secondNumber

    def divide(self, firstNumber, secondNumber):
        # Divide numbers
        return firstNumber / secondNumber


